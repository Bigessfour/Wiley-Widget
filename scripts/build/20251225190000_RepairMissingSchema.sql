-- Charges
IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME = 'Charges')
BEGIN
    CREATE TABLE [dbo].[Charges](
        [Id] INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
        [BillId] INT NOT NULL,
        [ChargeType] NVARCHAR(50) NOT NULL,
        [Description] NVARCHAR(200) NOT NULL,
        [Amount] DECIMAL(18,2) NOT NULL,
        [Quantity] DECIMAL(18,4) NOT NULL,
        [Rate] DECIMAL(18,4) NOT NULL,
        [CreatedDate] DATETIME2 NOT NULL,
        [LastModifiedDate] DATETIME2 NOT NULL,
        [UtilityBillId] INT NULL
    );
    IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE name = 'IX_Charges_BillId' AND object_id = OBJECT_ID('Charges'))
        CREATE INDEX IX_Charges_BillId ON Charges(BillId);
    IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE name = 'IX_Charges_ChargeType' AND object_id = OBJECT_ID('Charges'))
        CREATE INDEX IX_Charges_ChargeType ON Charges(ChargeType);
    IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE name = 'IX_Charges_UtilityBillId' AND object_id = OBJECT_ID('Charges'))
        CREATE INDEX IX_Charges_UtilityBillId ON Charges(UtilityBillId);
END
GO

